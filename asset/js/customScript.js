	function custom_validation_form(current_form_id){
		var current_form_id_new = "#"+current_form_id;
		
		$(  current_form_id_new+" .od , "+current_form_id_new+" .os" ).each(function( ) {
                $(this).css( "border", "1px solid #ddd" );
				$(this).closest('td').css( "border", "1px solid #ddd" );
		});

						
		var value_empty_found = false;
        $( "#"+current_form_id+" textarea" ).each(function( ) {
            var value_input  =$(this).val();
			var isdisabled= $(this).attr('disabled');
            if(typeof isdisabled === 'undefined'){
                if(value_input==''){
                    $(this).css( "border", "1px solid red" );
					value_empty_found = true;
                } 
			}
          });

        $( current_form_id_new+" .os , "+current_form_id_new+" .od" ).each(function( ) {
			if($(this).is(':radio')){
                	var element_name = $(this).attr('name');
					var isdisabled= $(this).attr('disabled');
           			if(typeof isdisabled === 'undefined'){
					  if($('[name="'+element_name+'"]:checked').length==0 ){
					  $(this).closest('td').css( "border", "3px solid red" );
						  value_empty_found = true;
					  }
					}
			}
			
            var value_input  = $(this).val();
			var isdisabled= $(this).attr('disabled');
            if(typeof isdisabled === 'undefined'){
			if(value_input==''){
                var element_name = $(this).attr('name');
                if( $(this).attr('type')=='text'){
                  $(this).css( "border", "1px solid red" );
					value_empty_found = true;
                }
             }
			}
          });
		  if(value_empty_found==true){
						  alert('All active fields are required. Kindly fill the form properly.');
				  return false;
		  }	
	
	}